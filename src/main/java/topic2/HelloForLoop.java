package topic2;

public class HelloForLoop {
    public static void main(String[] args) {
        // for (loop counter; condition; what to do after every iteration)
        // this loop will continue until the x is less than 10
        // x++ is shorthand for x=x+1
        for (int x = 0; x < 10; x++) {
            System.out.println(x);
        }
    }
}

