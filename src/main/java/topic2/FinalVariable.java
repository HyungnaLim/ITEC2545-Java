package topic2;

public class FinalVariable {
    public static void main(String[] args) {

        // when you put final in front of the variable type, the value of the variable won't change
        // also, it is common to write the final variable name in uppercase letters
        final int NUMBER_OF_DAYS = 7;

        // when trying to rewrite the final variable, error occur
        // NUMBER_OF_DAYS = 8;

    }
}
